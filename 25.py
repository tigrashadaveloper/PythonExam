def разбиениесписка(a3):
    # Сначала сортируем список A3
    a3.sort()

    # Разбиваем отсортированный список на две части
    n = len(a3) // 2
    b = a3[:n]
    c = a3[n:]

    return b, c

#Пример использования функции
A3 = [5, 2, 8, 3, 1, 7]  # Замените на ваш список A3
B, C = разбиениесписка(A3)

print("Список B:", B)
print("Список C:", C)
