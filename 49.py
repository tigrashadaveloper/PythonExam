class Студент:
    def init(self, имя):
        self.имя = имя
        self.дисциплины = {}  # Начально пустой словарь сданных дисциплин

    def put(self, названиедисциплины, оценка):
        """Добавляет новую дисциплину в атрибут Дисциплины."""
        self.__дисциплины[названиедисциплины] = оценка

    @property
    def Сдано(self):
        """Свойство, возвращающее список названий сданных дисциплин."""
        return list(self.__дисциплины.keys())

#Создаем экземпляр класса Студент
student = Студент("Иванов")

#Добавляем дисциплины и оценки
student.put("Математика", 4)
student.put("Физика", 5)
student.put("Информатика", 4)

#Выводим список сданных дисциплин с помощью свойства
print("Сданные дисциплины:", student.Сдано)
