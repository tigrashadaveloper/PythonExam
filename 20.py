class ПрофильМестности:
    def __init__(self, высоты):
        self.высоты = высоты

    def наибольшая_высота(self):
        return max(self.высоты)

    def наименьшая_высота(self):
        return min(self.высоты)

    def перепад_высот(self):
        return max(self.высоты) - min(self.высоты)

    def суммарный_перепад_высот(self):
        return sum(self.высоты) - len(self.высоты) * min(self.высоты)

    def крутизна(self):
        разницы_высот = [self.высоты[i] - self.высоты[i - 1] for i in range(1, len(self.высоты))]
        тангенсы = [разницы_высот[i] / 1 for i in range(len(разницы_высот))]
        return max(тангенсы), sum(тангенсы) / len(тангенсы)

    def сравнение_перепада(self, другой_профиль):
        перепад1 = self.перепад_высот()
        перепад2 = другой_профиль.перепад_высот()
        if перепад1 > перепад2:
            return "Перепад высоты в первом профиле больше"
        elif перепад1 < перепад2:
            return "Перепад высоты во втором профиле больше"
        else:
            return "Перепады высот равны"

    def сравнение_крутизны(self, другой_профиль):
        крутизна1 = self.крутизна()[0]
        крутизна2 = другой_профиль.крутизна()[0]
        if крутизна1 > крутизна2:
            return "Крутизна в первом профиле больше"
        elif крутизна1 < крутизна2:
            return "Крутизна во втором профиле больше"
        else:
            return "Крутизны равны"


# Пример использования класса
профиль1 = ПрофильМестности([10, 20, 30, 40, 50])
профиль2 = ПрофильМестности([5, 15, 25, 35, 45])

print(f"Наибольшая высота профиля 1: {профиль1.наибольшая_высота()}")
print(f"Наименьшая высота профиля 1: {профиль1.наименьшая_высота()}")
print(f"Перепад высот профиля 1: {профиль1.перепад_высот()}")
print(f"Суммарный перепад высот профиля 1: {профиль1.суммарный_перепад_высот()}")
print(f"Крутизна профиля 1: {профиль1.крутизна()}")
print(f"Сравнение перепада: {профиль1.сравнение_перепада(профиль2)}")
print(f"Сравнение крутизны: {профиль1.сравнение_крутизны(профиль2)}")