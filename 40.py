def суммапобочнойдиагонали(матрица):
    try:
        # Проверяем, что матрица является квадратной
        размер = len(матрица)
        for строка in матрица:
            if len(строка) != размер:
                raise ValueError("Матрица не является квадратной")

        # Вычисляем сумму побочной диагонали
        сумма = 0
        for i in range(размер):
            сумма += матрица[i][размер - 1 - i]

        return сумма
    except ValueError as e:
        print(f"Ошибка: {e}")
        return None

#Пример использования функции
мояматрица = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

сумма = суммапобочнойдиагонали(мояматрица)
if сумма is not None:
    print(f"Сумма побочной диагонали: {сумма}")
