def суммаглавнойдиагонали(матрица):
    try:
        # Проверяем, что матрица является квадратной
        размер = len(матрица)
        for строка in матрица:
            if len(строка) != размер:
                raise ValueError("Матрица не является квадратной")

        # Вычисляем сумму главной диагонали
        сумма = 0
        for i in range(размер):
            сумма += матрица[i][i]

        return сумма
    except ValueError as e:
        print(f"Ошибка: {e}")
        return None
